package com.practice.math;
import java.util.*;
public class SumOfNumbers {
		public static void main(String[] args) {
			
			Scanner sc = new Scanner(System.in);
			int a = sc.nextInt(),c = 0;
			
			for(int i=1;i<=a;i++)
			{
				 
		        c +=i;		
		         }
			 System.out.println(c);
			 sc.close();
			
		}
}
 